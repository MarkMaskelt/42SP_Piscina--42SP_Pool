int	ft_atoi(char *str)
{
	int	i;
	int	mul;
	int	c;

	mul = 1;
	i = 0;
	c = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			mul *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		c = (c * 10) + (str[i] - '0');
		i++;
	}
	return (c * mul);
}
