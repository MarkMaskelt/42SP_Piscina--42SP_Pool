#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c;

	if (nb < 0)
	{
		write (1, "-", 1);
		c = ((nb % 10) * -1) + '0';
	}
	else
	{
		c = (nb % 10) + '0';
	}
	nb = nb / 10;
	if (nb < 1)
	{
		nb = nb * -1;
	}
	if (nb != 0)
	{
		ft_putnbr(nb);
	}
	write(1, &c, 1);
}
